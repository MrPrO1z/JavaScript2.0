let out = document.querySelector('.out');
console.log(out);

for (let i = 0; i < 5; i++) {
    // i=0 i станет равно нулю 
    for (let k = 0; k < 10; k++) {
        // k=0  k станет равно нулю и будет выведена звездочка
        out.innerHTML += '*';
    }
    out.innerHTML += '<br>';
}
////////////////////////////////////////////
// таблица умножения при помощи вложенного цикла
for (let i = 1; i <= 9; i++) {
    for (let k = 1; k < 10; k++) {
        // out.innerHTML += '3*' + i + '=' + (i * 3) + '<br>'; таблица умножения
        out.innerHTML += `${i}*${k}=${k * i}<br>`;  // таже строка что и выше при помощи интерполяции
    }
    out.innerHTML += '<hr>';
}

