//    Решить задачу с помощью вложенных циклов. Внешний
//   цикл выводит цифры и перенос строки
//    br, , второй звездочки, знак подчеркивания и знак
//     переноса.
let out1 = document.querySelector('.out1');
for (let i=1; i <= 3; i++){
    
    out1.innerHTML += `${i}<br>`;
    
    for (let k = 0; k < 1; k++){
        out1.innerHTML += `*_*_*_<br>`;  
        
    }
}

